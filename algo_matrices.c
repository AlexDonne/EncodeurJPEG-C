void afficher_matrice8x8_8(uint8_t *matrice) {
  for (int i = 0; i < 8; i++) {
    for (int j = 0; j < 8; j++) {
      printf("%x ",matrice[i*8 + j]);
    }
    printf("\n");
  }
  printf("\n");
}

void afficher_matrice8x8_16(uint16_t *matrice) {
  for (int i = 0; i < 8; i++) {
    for (int j = 0; j < 8; j++) {
      printf("%4.4x ",matrice[i*8 + j]);
    }
    printf("\n");
  }
  printf("\n");
}

